# Zad4. Napisz skrypt, który policzy i wyświetli następujące wyrażenia: ...

import math

# Euler number to the power of 10 (e^10)
a = math.exp(10)

print(a)  # 22026.465794806718

# square root of ...
b = math.pow(math.log10(5 + math.pow(math.sin(8), 2)), 1/2)

print(b)  # 0.881258297062307

# Floor
c = math.floor(3.55)

print(c)  # 3

# Ceil
d = math.ceil(4.80)

print(d)  # 5
